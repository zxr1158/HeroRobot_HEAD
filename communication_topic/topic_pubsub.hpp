/**
 * @file topic_pubsub.hpp
 * @brief Topic 系统的轻量包装：Publisher/Sub
 *
 * 设计思路：
 * =========
 * - `Publisher<T>`：只暴露 publish 能力，强化“发布者只写不读”的语义。
 * - `Sub<T>`：对 `Subscription<T>` 的短名字封装，减少模板噪音。
 *
 * 边界：
 * =====
 * - 这是语义层封装，不改变底层 `Topic/Subscription` 的线程安全与内存序假设。
 * - 仍遵循单写多读模型：同一 Topic 建议只有一个发布者。
 */

#pragma once

#include "topic.hpp"

/*******************************************************************************
 * Publisher<T> - 发布者包装类
 * ----------------------------------------------------------------------------
 *
 * 【功能】
 *   对 Topic 发布操作的简单封装，提供更清晰的语义。
 *
 * 【设计目的】
 *   - 明确发布者的角色（区分于订阅者）
 *   - 限制发布者只能进行发布操作，不能读取
 *   - 便于代码审查和维护
 *
 * 【使用示例】
 *   // 在发布者模块中
 *   Publisher<MotorCmd> motor_pub(orb::motor_cmd);
 *
 *   void control_loop() {
 *       MotorCmd cmd = calculate_motor_command();
 *       motor_pub.publish(cmd);
 *   }
 *
 * 【注意】
 *   这只是一个轻量级的包装类，没有额外的性能开销。
 *   如果觉得多余，也可以直接使用 Topic::publish()。
 *
 ******************************************************************************/
template<typename T>
class Publisher {
public:
    /***************************************************************************
     * 构造函数
     * -------------------------------------------------------------------------
     * @param topic 要发布到的 Topic 引用
     **************************************************************************/
    explicit Publisher(Topic<T>& topic)
        : topic_(topic)
    {}

    /***************************************************************************
     * publish - 发布数据
     * -------------------------------------------------------------------------
     * @param data 要发布的数据
     **************************************************************************/
    inline void publish(const T& data)
    {
        topic_.publish(data);
    }

private:
    Topic<T>& topic_;  // 目标 Topic 引用
};


/*******************************************************************************
 * Sub<T> - 订阅者包装类
 * ----------------------------------------------------------------------------
 *
 * 【功能】
 *   对 Subscription 的简单包装，提供更简洁的接口名称。
 *
 * 【与 Subscription 的区别】
 *   - Sub 是 Subscription 的别名/包装
 *   - 接口完全相同，只是名字更短
 *   - 内部创建一个 Subscription 对象
 *
 * 【使用示例】
 *   // 在订阅者模块中
 *   Sub<MotorCmd> motor_sub(orb::motor_cmd);
 *
 *   void task_loop() {
 *       MotorCmd cmd;
 *       if (motor_sub.copy(cmd)) {
 *           apply_motor_command(cmd);
 *       }
 *   }
 *
 ******************************************************************************/
template<typename T>
class Sub {
public:
    /***************************************************************************
     * 构造函数
     * -------------------------------------------------------------------------
     * @param topic 要订阅的 Topic 引用
     **************************************************************************/
    explicit Sub(Topic<T>& topic)
        : sub_(topic)
    {}

    /***************************************************************************
     * updated - 检查是否有新数据
     * -------------------------------------------------------------------------
     * @return true 表示有未读的新数据
     **************************************************************************/
    inline bool updated()
    {
        return sub_.updated();
    }

    /***************************************************************************
     * copy - 拷贝新数据
     * -------------------------------------------------------------------------
     * @param out 输出参数
     * @return true 表示成功拷贝了新数据
     **************************************************************************/
    inline bool copy(T& out)
    {
        return sub_.copy(out);
    }

private:
    Subscription<T> sub_;  // 内部的 Subscription 对象
};
